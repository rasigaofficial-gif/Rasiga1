function getRasigaCopySet() {
  var lang = (navigator.language || navigator.userLanguage || '').toLowerCase();
  var tz = '';
  try { tz = Intl.DateTimeFormat().resolvedOptions().timeZone || ''; } catch(e) {}
  var tz_lower = tz.toLowerCase();

  if (lang.startsWith('ta')) return RASIGA_COPY.tamil;
  if (lang.startsWith('te')) return RASIGA_COPY.telugu;
  if (lang.startsWith('ml')) return RASIGA_COPY.malayalam;
  if (lang.startsWith('kn')) return RASIGA_COPY.kannada;
  if (lang.startsWith('pa')) return RASIGA_COPY.punjabi;
  if (lang.startsWith('bn')) return RASIGA_COPY.bengali;

  if (tz_lower.indexOf('dubai') > -1 || tz_lower.indexOf('gulf') > -1 ||
      tz_lower.indexOf('riyadh') > -1 || tz_lower.indexOf('kuwait') > -1 ||
      tz_lower.indexOf('doha') > -1 || tz_lower.indexOf('muscat') > -1)
    return RASIGA_COPY.diaspora_gulf;

  if (tz_lower.indexOf('london') > -1 || tz_lower.indexOf('europe') > -1)
    return RASIGA_COPY.diaspora_uk;

  if (tz_lower.indexOf('america') > -1 || tz_lower.indexOf('us/') > -1 ||
      tz_lower.indexOf('canada') > -1 || tz_lower.indexOf('toronto') > -1 ||
      tz_lower.indexOf('vancouver') > -1)
    return RASIGA_COPY.diaspora_us;

  if (tz_lower.indexOf('kolkata') > -1 || tz_lower.indexOf('india') > -1) {
    if (lang.startsWith('hi')) return RASIGA_COPY.hindi;
    return RASIGA_COPY.universal;
  }

  return RASIGA_COPY.universal;
}

function getRandomCopy() {
  var set = getRasigaCopySet();
  return set[Math.floor(Math.random() * set.length)];
}

/**
 * Applies keyword highlighting, punchline styling, and staggered animation
 * to the hero headline, subtitle, and CTA button.
 */
function applyHeroCopy(copy) {
  var headlineEl = document.querySelector('.hero-title');
  var sublineEl = document.querySelector('.hero-sub');
  var ctaEl = document.getElementById('cta-btn');

  if (!headlineEl || !copy) return;

  // --- HEADLINE: Keyword highlight + punchline split + stagger ---
  var text = copy.h;
  var keywords = copy.k || [];

  // Split into sentences
  var sentences = text.match(/[^.!?]+[.!?]+/g) || [text];
  var punchline = '';
  if (sentences.length > 1) {
    punchline = sentences.pop().trim();
  }

  // Highlight keywords in a sentence string
  function highlightKeywords(str) {
    var result = str;
    // Sort keywords longest first so "Chaiyya Chaiyya" matches before "Chaiyya"
    var sorted = keywords.slice().sort(function(a, b) { return b.length - a.length; });
    sorted.forEach(function(kw) {
      // Escape special regex characters in keyword
      var escaped = kw.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
      var regex = new RegExp('(' + escaped + ')', 'gi');
      result = result.replace(regex, '<span class="hero-keyword">$1</span>');
    });
    return result;
  }

  var html = '';
  var delay = 0;
  sentences.forEach(function(sentence) {
    delay += 120;
    html += '<span class="hero-sentence" style="animation-delay:' + delay + 'ms">' + highlightKeywords(sentence.trim()) + ' </span>';
  });

  if (punchline) {
    delay += 250;
    html += '<span class="hero-punchline hero-sentence" style="animation-delay:' + delay + 'ms">' + highlightKeywords(punchline) + '</span>';
  }

  headlineEl.innerHTML = html;

  // --- SUBTITLE: fade in ---
  if (sublineEl) {
    delay += 200;
    sublineEl.innerHTML = '<span class="hero-sentence" style="animation-delay:' + delay + 'ms">' + copy.s + '</span>';
  }

  // --- CTA: pop in last ---
  if (ctaEl) {
    delay += 200;
    ctaEl.textContent = copy.cta;
    ctaEl.style.opacity = '0';
    ctaEl.style.animation = 'heroWordPop 0.5s cubic-bezier(0.2, 0.8, 0.2, 1) ' + delay + 'ms forwards';
  }
}
